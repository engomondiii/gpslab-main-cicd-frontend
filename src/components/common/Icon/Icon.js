/**
 * Icon component
 * 
 * @param {Object} props - Component props
 * @param {string} props.name - Icon name
 * @param {string} [props.size='md'] - Icon size
 * @param {string} [props.color] - Icon color
 * @param {string} [props.className] - Additional CSS classes
 */
const Icon = ({
  name,
  size = ICON_SIZES.MD,
  color,
  className = '',
  ...props
}) => {
  
  const path = ICONS[name];
  
  if (!path) {
    console.warn(`Icon "${name}" not found`);
    return null;
  }
  
  const classNames = [
    'icon',
    `icon--${size}`,
    className
  ].filter(Boolean).join(' ');
  
  return (
    <svg
      className={classNames}
      viewBox="0 0 24 24"
      fill="none"
      stroke={color || 'currentColor'}
      strokeWidth="2"
      strokeLinecap="round"
      strokeLinejoin="round"
      aria-hidden="true"
      {...props}
    >
      <path d={path} />
    </svg>
  );
};

